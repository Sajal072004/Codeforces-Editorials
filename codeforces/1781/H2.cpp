const int N = 2E5 + 5;

using mint = modint998244353;

mint fib[N], inv_fib[N];

struct dsu {
    vector<int> par, val;
    dsu(int n) : par(n, -1), val(n, 0) {}

    int trace(int u) { return par[u] < 0 ? u : par[u] = trace(par[u]); }

    int free_nodes(int u) {
        u = trace(u);
        return -par[u] - val[u];
    }

    mint value(int u) { return fib[free_nodes(u) + 1]; }

    mint inverse_value(int u) { return inv_fib[free_nodes(u) + 1]; }

    mint connect(int u, int v) {
        if ((u = trace(u)) == (v = trace(v))) {
            return 1;
        }
        if (par[u] > par[v]) {
            swap(u, v);
        }
        mint den = inverse_value(u) * inverse_value(v);
        par[u] += par[v]; par[v] = u;
        val[u] += val[v];
        mint num = value(u);
        return num * den;
    }

    mint update(int u, int add) {
        u = trace(u);
        mint den = inverse_value(u);
        val[u] += add;
        mint num = value(u);
        return num * den;
    }
};

bool in_box(int h, int w, int x, int y) { return x >= 0 && x < h && y >= 0 && y < w; }

mint solve(int h, int w, vector<pair<int, int>> bad) {
    mint tot = 0;
    vector affect(h + 1, vector<vector<pair<int, int>>>(w + 1));
    vector val(h + 1, vector<int>(w + 1));
    dsu dsu(h * w);
    for (int bh = 1; bh <= h; bh++) {
        int mh = h - bh, free_edge = 0;
        // clearing stuff
        for (int i = 0; i <= mh; i++) {
            for (int j = 0; j <= w; j++) {
                val[i][j] = 0;
                affect[i][j].clear();
            }
        }
        for (int i = 0; i < bh * w; i++) {
            dsu.par[i] = -1; dsu.val[i] = 0;
        }
        mint bad_cases = mint(2).pow(bh * w);
        for (int bw = w; bw >= 1; bw--) {
            int mw = w - bw;
            mint ans = mint(2).pow(bh * bw);
            // dealing with bounding box [bh, bw], moving box [mh, mw]
            // add edges [0 -> mh, mw]
            for (int move_h = 0, move_w = mw; move_h <= mh; move_h++) {
                vector<pair<int, int>>& xd = affect[move_h][move_w];
                for (auto [x, y] : bad) {
                    x -= move_h; y -= move_w;
                    if (in_box(bh, bw, x, y)) {
                        xd.push_back({x, y});
                        val[move_h][move_w]++;
                    }
                }
                if (xd.empty()) {
                    free_edge++;
                } else if (xd.size() == 1) {
                    bad_cases *= dsu.update(unwrap(xd[0]), 1);
                } else {
                    bad_cases *= dsu.connect(unwrap(xd[0]), unwrap(xd[1]));
                }
            }
            tot += ans - (free_edge ? 0 : bad_cases);
            // remove edges related to [0 -> bh - 1, bw - 1]
            for (int bound_h = 0, bound_w = bw - 1; bound_h < bh; bound_h++) {
                bad_cases *= dsu.update(unwrap(pair(bound_h, bound_w)), 1);
                for (auto [x, y] : bad) {
                    int move_h = x - bound_h, move_w = y - bound_w;
                    if (in_box(mh + 1, mw + 1, move_h, move_w)) {
                        bad_cases *= dsu.update(unwrap(affect[move_h][move_w][0]), 1);
                        if (--val[move_h][move_w] == 0) {
                            free_edge++;
                        }
                    }
                }
            }
        }
    }
    return tot;
}

