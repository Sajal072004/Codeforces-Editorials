ll help(string &s,ll idx,ll rem){
    if(idx>=s.length()) return 0;
    if(dp[idx][rem]!=-1) return dp[idx][rem];
    ll take=0,nottake=0;
    take=help(s,idx+1,(rem*10+(s[idx]-'0'))%3);
    nottake=help(s,idx+1,0)+((rem+s[idx]-'0')%3==0);
    return dp[idx][rem]=max(take,nottake);

}
