#define IOS ios_base::sync_with_stdio(0);cin.tie(0);cout.tie(0)
#define ll long long
using namespace std;
int main() {
    IOS;
    int k; cin >> k;
    string s; cin >> s;
    int n = s.size();
    set<char> got;
    for (int i = 0; i < n; i++) got.insert(s[i]);
    if (got.size() < k) cout << "NO";
    else {
        got.clear();
        cout << "YES";
        for (int i = 0; i < n; i++) {
            if (k == 0) {
                for (int j = i; j < n; j++) cout << s[j];
                break;
            }
            else {
                if (got.find(s[i]) == got.end()) { //找不到
                    got.insert(s[i]);
                    cout << '\n' << s[i];
                    k--;
                }
                else //找到了
                    cout << s[i];
            }
        }
    }
}
