LL gcd(LL a, LL b)
{
    return b ? gcd(b, a % b) : a;
}
LL lcm(LL a, LL b)
{
    return a * b / gcd(a, b);
}
LL exgcd(LL a, LL b, LL &x, LL &y)
{
    if (!b)
    {
        x = 1;
        y = 0;
        return a;
    }
    LL d = exgcd(b, a % b, y, x);
    y -= (a / b) * x;
    return d;
} // ax + by = gcd(a,b);返回值为gcd(a,b)

LL qpow(LL x, LL n)
{
    LL ans = 1;
    while (n)
    {
        if (n & 1)
            ans *= x;
        x *= x;
        n >>= 1;
    }
    return ans;
}
bool is_prime(LL x)
{
    if (x < 2)
        return false;
    for (LL i = 2; i <= x / i; i++)
        if (x % i == 0)
            return false;
    return true;
}