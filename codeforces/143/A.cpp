bool she(int n, int d)
{
    vector<int>v;
    for (int i = 1; i <= n / i; i++)
    {
        if (n % i == 0)
        {
            v.push_back(i);
            if (i != n / i)
            {
                v.push_back(n / i);
            }
        }
    }sort(v.begin(), v.end());
    for (int i = 1; i < v.size(); i++)
    {
        if (v[i] - v[i - 1] < d)
        {
            return 0;
        }
    }
    return 1;
}
