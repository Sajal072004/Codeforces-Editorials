int spf(int n)
{
    vector<int>v(n + 1);
    for (int i = 2; i <= n; i++)
    {
        v[i] = i;
    }

    for (int i = 2; i * i <= n; i++)
    {
        if (v[i] == i)
        {
            for (int j = i + i; j <= n; j += i)
            {
                if (v[j] == j)
                {
                    v[j] = i;
                }
            }
        }
    }
    return v[n];


}

