using namespace std;
typedef long long ll;
const int maxn = 2e6 + 7;
const int N = 2e5+7;
const ll INF = 1e18;
ll n, m, k, x, y, z;
#define ls p << 1
#define rs p << 1 | 1
#define Pa pair<ll, ll>
#define YES cout << "YES\n"
