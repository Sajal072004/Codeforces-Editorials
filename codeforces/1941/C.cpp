int random(int l, int r)
{
    return l + rd() % (r - l + 1);
}

int sol()
{
    int n; cin >> n;
    string s; cin >> s;
    int ans = 0;
    for (int i = 1; i < n - 1; ++i)
    if ((s[i - 1] == 'm' && s[i] == 'a' && s[i + 1] == 'p') || (s[i - 1] == 'p' && s[i] == 'i' && s[i + 1] == 'e')) ans++;

    for (int i = 2; i < n - 2; ++i)
    if (s[i - 2] == 'm' && s[i - 1] == 'a' && s[i] == 'p' && s[i + 1] == 'i' && s[i + 2] == 'e') ans--;
    return ans;
}

