void solve(){
  string s;
  cin>>s;
  int n = s.length();
  string result;

  for(int i = 0; i<n; i++){
    if(i<n-1 && s[i+1]=='.' && s[i] == '-'){
      result.push_back('1');
      i++;
    }
    else if(i<n-1 && s[i+1]=='-' && s[i] == '-'){
      result.push_back('2');
      i++;
    }
    else{
      result.push_back('0');
    }
  }
  cout<<result<<endl;
}

